using System.Security.Cryptography;
using System.Text;
using EcommerceAPI.Core.Interfaces;
using Microsoft.Extensions.Configuration;

namespace EcommerceAPI.Business.Concrete;

public class EncryptionService : IEncryptionService
{
    private readonly byte[] _key;
    private const int NonceSize = 12;
    private const int TagSize = 16;

    public EncryptionService(IConfiguration configuration)
    {
        var keyString = configuration["ENCRYPTION_KEY"] 
            ?? throw new InvalidOperationException("ENCRYPTION_KEY environment variable is not set. Please set a 32-character key for AES-256.");
        
        if (keyString.Length != 32)
        {
            throw new InvalidOperationException($"ENCRYPTION_KEY must be exactly 32 characters for AES-256. Current length: {keyString.Length}");
        }
        
        _key = Encoding.UTF8.GetBytes(keyString);
    }

    public string Encrypt(string plainText)
    {
        if (string.IsNullOrEmpty(plainText))
            return plainText;

        var plainBytes = Encoding.UTF8.GetBytes(plainText);
        var nonce = new byte[NonceSize];
        RandomNumberGenerator.Fill(nonce);

        var cipherText = new byte[plainBytes.Length];
        var tag = new byte[TagSize];

        using var aesGcm = new AesGcm(_key, TagSize);
        aesGcm.Encrypt(nonce, plainBytes, cipherText, tag);

        var result = new byte[NonceSize + TagSize + cipherText.Length];
        Buffer.BlockCopy(nonce, 0, result, 0, NonceSize);
        Buffer.BlockCopy(tag, 0, result, NonceSize, TagSize);
        Buffer.BlockCopy(cipherText, 0, result, NonceSize + TagSize, cipherText.Length);

        return Convert.ToBase64String(result);
    }

    public string Decrypt(string cipherText)
    {
        if (string.IsNullOrEmpty(cipherText))
            return cipherText;

        try
        {
            var fullCipher = Convert.FromBase64String(cipherText);

            if (fullCipher.Length < NonceSize + TagSize)
            {
                return cipherText;
            }

            var nonce = new byte[NonceSize];
            var tag = new byte[TagSize];
            var cipherBytes = new byte[fullCipher.Length - NonceSize - TagSize];

            Buffer.BlockCopy(fullCipher, 0, nonce, 0, NonceSize);
            Buffer.BlockCopy(fullCipher, NonceSize, tag, 0, TagSize);
            Buffer.BlockCopy(fullCipher, NonceSize + TagSize, cipherBytes, 0, cipherBytes.Length);

            var plainBytes = new byte[cipherBytes.Length];

            using var aesGcm = new AesGcm(_key, TagSize);
            aesGcm.Decrypt(nonce, cipherBytes, tag, plainBytes);

            return Encoding.UTF8.GetString(plainBytes);
        }
        catch (FormatException)
        {
            return cipherText;
        }
        catch (CryptographicException)
        {
            throw new InvalidOperationException("Veri çözülemedi. Encryption key değişmiş olabilir.");
        }
    }
}
